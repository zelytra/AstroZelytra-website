            <p><strong>Description:</strong><br>Les Pléiades, ou amas M45, sont un amas ouvert d'étoiles qui s'observe depuis les deux hémisphères, dans la constellation du Taureau. Il existe un débat concernant l'exactitude de la distance de l'amas à la Terre. Cette distance, selon les différents instruments techniques utilisés, pourrait être estimée à environ 444 années-lumière.<br>On dénombre aujourd'hui dans cet amas environ 3 000 étoiles, dont une douzaine sont visibles à l'œil nu. Il s'étend sur 2°, soit l'équivalent de quatre fois le diamètre apparent de la Lune. Sa densité est donc relativement faible par rapport aux autres amas ouverts. L'âge de l'amas est estimé à 100 millions d'années, mais il ne devrait pas vivre longtemps puisqu'il devrait se séparer dans 250 millions d'années, en partie à cause de sa faible densité (il s'agit ici de la vie de l'amas et non de celle des étoiles qui le composent).</p>
