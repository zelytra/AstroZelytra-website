            <h1>M08: Une randonée chargé...</h1> 

            <p>Nous voilà devant la nébuleuse de la Trifide et de la Lagune. Ces deux objets se situe dans le même axe que notre voie lacté ce qui donne cette teinte doré sur la photo. Pour réaliser cette photo j’ai dû m’enfoncer à l’intérieur des terres de l’île de Fuerteventura. Avec tout mon matériel photo et astro j’en ais pour un peu prêts 15 kilo de matériel et pour accéder à la zone où je suis allé j’ai dû faire 8km. Ce fut long et fastidieux mais une fois sur place ce n’est que du bonheur. Admirant le ciel dans un noir quasi-totale. C’est ce soir-là que j’ai pu observé mon premier bolide qui laissa derrière lui une traînée de fumée dû à sa vaporisation en entrant dans l’atmosphère. C’est un phénomène rare à regarder et qui dure de 5 à 10 secondes selon la taille du bolide !</p>
