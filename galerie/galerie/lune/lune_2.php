            <h1>Eclipse de Lune</h1> 

            <p>Bonsoir, comme vous en avez surement entendu parler lors des informations, dans la matinée du 21.01.2018 à 5hs44 ont eu lieu une éclipse de Lune totale ! Ce type d’éclipse ne se reproduira pas avant le 16 juillet 2022. Pour ceux qui ne comprennent pas trop ce phénomène je vais le résumer brièvement. Tout d’abord vous savez qu’une éclipse solaire correspond à un alignement Soleil-Lune-Terre : du point de vue de la terre la Lune couvre le Soleil ce qui crée un grand ombre sur la Terre. Une éclipse de Lune c’est la même chose sauf que l’alignement est différent : Soleil-Terre-Lune, en effet ici la terre fait office de parasol à la Lune ce qui fait qu’elle devient noire.. Mais une question se pose pourquoi est-elle rouge sur les photos ?! Quand vous observer un coucher de soleil le ciel devient orangé puis rouge puis il fait nuit… Et bien c’est pareil pour la Lune le fait qu’elle devient rouge est dû à l’atmosphère de la Terre qui ne va pas laisser passer uniquement ces couleurs en direction de la Lune. Ce qui explique cette teinte ! Je tiens à rappeler que les termes « Lune de sang, Super Lune… » Et j’en passe ne sont que des erreurs inventer par les médias pour faire des titres aguicheurs. Le vrai terme (scientifique) de cet évènement est Éclipse de Lune.</p>
