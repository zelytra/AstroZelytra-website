            <p><strong>Description:</strong><br>La Lune, ou Terre I, est un objet céleste qui orbite autour de la planète Terre et le seul satellite naturel permanent de la Terre. C'est le cinquième plus grand satellite naturel du Système solaire et le plus grand des satellites planétaires par rapport à la taille de la planète autour de laquelle elle orbite. La Lune est, après le satellite de Jupiter Io, le deuxième satellite le plus dense du Système solaire parmi ceux dont la densité est connue.<br>On pense que la Lune s'est formée il y a environ 4,51 milliards d'années, peu de temps après la Terre. L'explication la plus largement acceptée est que la Lune s'est formée à partir des débris restants après un impact géant entre la Terre et un corps de la taille de Mars appelé Théia.</p>
