            <h1>M45: M45: Des détails inatendues...</h1> 

            <p>Voici M45 plus communément appelé « Les pléiades ». C’est un amas ouvert se situant seulement à approximativement 444 années lumières. A l’ordre ne notre échelle elle se situe très loin mais à l’échelle de l’univers elle se trouve à côté. Comme dis précédemment c’est un amas ouvert, mais qu’est qu’un amas ouvert : Tout d’abord un amas d’étoile est un rassemblement d’étoile en un point compacte (quand je dis compacte cela varie entre 5.4  années lumières à 54 années-lumière de diamètre ce qui est compacte à l’échelle de l’univers…) et le fait qu’elle soit « ouverte » signifie qu’elle ne contient qu’entre 100 et 1000 étoiles tans dit que sont opposé : l’amas Globulaire comprend plus d’une centaine de millier d’étoiles… Donc maintenant vous savez ce qu’est un amas ouvert d’étoile et que l’amas des Pléiades en fait partie. Cet amas peut s’observer la nuit très facilement sans avoir besoin de matériel d’observations. Il vous suffit uniquement de lever les yeux au ciel et de chercher un endroit où se trouvent 5-6 étoiles très brillantes et condensées dans le ciel et bingo ! Bien entendu vous ne pourrez pas observer les nuages de gaz qui cette amas à l’œil nue !  Funfacte : ce nuage de gaz et de poussière ne fait pas partie de cet amas ! L’amas a tout simplement croisé cette nébuleuse lors de son déplacement dans le vide spatial, sacrés coïncidence non ?! Et ce qui donne cette teinte bleu vient des étoiles venant de cette amas qui sont très chaude (rappelle dans notre monde ce qui est bleu est extrêmement chaud et ce qui est rouge est relativement chaud).</p>
